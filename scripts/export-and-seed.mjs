// Export all users + events from the live Heroku API and generate a D1 seed
// SQL file. Run: node scripts/export-and-seed.mjs
// Then load it:  wrangler d1 execute guardguys --remote --file=./data-export/seed.sql
//
// Notes:
// - Preserves integer IDs and bcrypt password hashes.
// - Booleans (isAdmin, onsite) -> 0/1 for SQLite.
// - Events whose user_id points at a non-existent user are set to NULL so the
//   foreign key holds (those users were deleted; the API already returns no
//   user object for them).

import { writeFile, mkdir } from "node:fs/promises";

const API = "https://guardguys.herokuapp.com";
const OUT_DIR = "data-export";
const OUT_FILE = `${OUT_DIR}/seed.sql`;
const BATCH = 200;

const q = (v) => {
  if (v === null || v === undefined) return "NULL";
  if (typeof v === "number") return String(v);
  if (typeof v === "boolean") return v ? "1" : "0";
  return `'${String(v).replace(/'/g, "''")}'`;
};

async function getJson(path) {
  const res = await fetch(`${API}${path}`);
  if (!res.ok) throw new Error(`GET ${path} -> ${res.status}`);
  return res.json();
}

function batchInsert(table, columns, rows) {
  const out = [];
  for (let i = 0; i < rows.length; i += BATCH) {
    const chunk = rows.slice(i, i + BATCH);
    const values = chunk.map((r) => `(${r.join(",")})`).join(",\n  ");
    out.push(
      `INSERT INTO ${table} (${columns.join(", ")}) VALUES\n  ${values};`,
    );
  }
  return out.join("\n\n");
}

async function main() {
  console.log("Fetching users…");
  const users = await getJson("/api/users/");
  console.log(`  ${users.length} users`);

  console.log("Fetching events…");
  const events = await getJson("/api/events/");
  console.log(`  ${events.length} events`);

  const userIds = new Set(users.map((u) => u.id));
  let orphaned = 0;

  const userRows = users.map((u) => [
    q(u.id),
    q(u.username),
    q(u.email),
    q(u.password),
    q(!!u.isAdmin),
    q(u.createdAt),
    q(u.updatedAt),
  ]);

  const eventRows = events.map((e) => {
    let userId = e.user_id ?? null;
    if (userId !== null && !userIds.has(userId)) {
      orphaned++;
      userId = null;
    }
    return [
      q(e.id),
      q(e.date),
      q(e.event ?? ""),
      q(!!e.onsite),
      q(e.notes ?? ""),
      q(typeof e.duration === "number" ? e.duration : 0),
      q(userId),
      q(e.createdAt),
      q(e.updatedAt),
    ];
  });

  const sql = [
    "-- Generated by scripts/export-and-seed.mjs — DO NOT COMMIT (contains password hashes/PII).",
    "PRAGMA foreign_keys=OFF;",
    "DELETE FROM events;",
    "DELETE FROM users;",
    "",
    batchInsert(
      "users",
      ["id", "username", "email", "password", "isAdmin", "createdAt", "updatedAt"],
      userRows,
    ),
    "",
    batchInsert(
      "events",
      ["id", "date", "event", "onsite", "notes", "duration", "user_id", "createdAt", "updatedAt"],
      eventRows,
    ),
    "",
  ].join("\n");

  await mkdir(OUT_DIR, { recursive: true });
  await writeFile(OUT_FILE, sql);
  console.log(
    `Wrote ${OUT_FILE} (${userRows.length} users, ${eventRows.length} events, ${orphaned} orphaned user_id -> NULL)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
